/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	uint32_t step = (tmc_timers[TMC_TIMER1].steps[0] & 0x7F - 1) & 0x7F;
	htim1.Instance->RCR = step;
	step = step + 1;
	htim1.Instance->CCR1 = tmc_timers[TMC_TIMER1].steps[0]?100:0;
	tmc_timers[TMC_TIMER1].steps[0] -= step;
	__HAL_TIM_CLEAR_IT(&htim1, TIM_IT_UPDATE);
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

/**
  * @brief This function handles TIM4 global interrupt.
  */
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
  htim4.Instance->CCR1 = tmc_timers[TMC_TIMER4].steps[0]?100:0;
	htim4.Instance->CCR2 = tmc_timers[TMC_TIMER4].steps[1]?100:0;
	htim4.Instance->CCR3 = tmc_timers[TMC_TIMER4].steps[2]?100:0;
	htim4.Instance->CCR4 = tmc_timers[TMC_TIMER4].steps[3]?100:0;
	tmc_timers[TMC_TIMER4].steps[0] -= tmc_timers[TMC_TIMER4].steps[0]>0 ? 1 : 0;
	tmc_timers[TMC_TIMER4].steps[1] -= tmc_timers[TMC_TIMER4].steps[1]>0 ? 1 : 0;
	tmc_timers[TMC_TIMER4].steps[2] -= tmc_timers[TMC_TIMER4].steps[2]>0 ? 1 : 0;
	tmc_timers[TMC_TIMER4].steps[3] -= tmc_timers[TMC_TIMER4].steps[3]>0 ? 1 : 0;
	__HAL_TIM_CLEAR_IT(&htim4, TIM_IT_UPDATE);
  /* USER CODE END TIM4_IRQn 0 */
  /* USER CODE BEGIN TIM4_IRQn 1 */

  /* USER CODE END TIM4_IRQn 1 */
}

